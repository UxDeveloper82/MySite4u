        // GET: api/Post
        [HttpGet]
        public async Task<ActionResult<IEnumerable<Post>>> GetPosts([FromQuery] PostParams postParams)
        {
            var posts = await _repo.GetPostsAsync(postParams);

            Response.AddPaginationHeader(posts.CurrentPage, posts.PageSize,
                posts.TotalCount, posts.TotalPages);
            return Ok(posts);
        }

